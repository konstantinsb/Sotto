import Foundation

/// Drop-to-live: защита потоковой расшифровки от накопления задержки.
///
/// Проблема: движок расшифровки (Whisper) на тяжёлой модели идёт медленнее реального
/// времени. Аудио приходит в реальном времени, и между захватом и движком стоят
/// `AsyncStream` с буфером `.unbounded` — поэтому необработанные чанки копятся без
/// предела. За несколько минут отставание доходит до минут: подсказка приходит на то,
/// что сказали давно. Для live-ассистента это бесполезно.
///
/// Решение: ограничить очередь к движку. Когда движок отстаёт — выбрасывать самые
/// старые чанки (а не копить), чтобы он всегда работал по «сейчас». Цена — потеря части
/// аудио при перегрузке (лучше актуально-с-пропусками, чем полно-но-с-минутной-задержкой).
/// Полностью убирает потери только облегчение модели (движок быстрее реального времени).
public enum AudioBackpressure {

    /// Сколько 0.1-с чанков держим в очереди к движку, пока он занят.
    /// Сверх лимита самые старые выбрасываются. 40 чанков ≈ 4 с — это потолок задержки
    /// расшифровки: компромисс между «свежестью» и тем, чтобы окно успевало набраться.
    public static let defaultMaxQueuedChunks = 40

    /// Ограничить очередь аудио к (возможно медленному) движку. При отставании consumer'а
    /// старые чанки выбрасываются (`.bufferingNewest`), движок получает самые свежие.
    /// Дренаж-таск читает источник в реальном времени и перекладывает в ограниченный
    /// буфер — поэтому исходный `unbounded`-буфер не копится.
    public static func dropToLive(
        _ source: AsyncStream<AudioChunk>,
        maxChunks: Int = defaultMaxQueuedChunks
    ) -> AsyncStream<AudioChunk> {
        AsyncStream<AudioChunk>(bufferingPolicy: .bufferingNewest(maxChunks)) { continuation in
            let task = Task {
                for await chunk in source { continuation.yield(chunk) }
                continuation.finish()
            }
            continuation.onTermination = { _ in task.cancel() }
        }
    }

    /// Был ли разрыв в потоке между двумя соседними чанками — то есть drop-to-live
    /// выбросил кусок аудио. `timestamp` чанка — его старт в аудио-времени; у непрерывных
    /// чанков старт следующего = старт предыдущего + его длительность. При дропе старт
    /// прыгает вперёд сильнее. Движок по этому признаку начинает новое окно, чтобы не
    /// склеивать несмежные куски речи (иначе расшифровка мусорится).
    public static func isDiscontinuity(
        previousStart: TimeInterval,
        previousDuration: TimeInterval,
        nextStart: TimeInterval,
        tolerance: TimeInterval = 0.15
    ) -> Bool {
        nextStart - (previousStart + previousDuration) > tolerance
    }
}
